#include "libft.h"

void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int temp;
	int echange;

	i = 0;
	echange = 1;
	while (echange)
	{
		echange = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
				echange = 1;
			}
			i++;
		}
		i = 0;
	}
}
